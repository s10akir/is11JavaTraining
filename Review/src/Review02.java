public class Review02 {
    public static void main(String[] args) {
        System.out.println("7 + 2 = " + (7 + 2));
        System.out.println("7 - 2 = " + (7 - 2));
        System.out.println("7 * 2 = " + (7 * 2));
        System.out.println("7 ÷ 2 = " + (7 / 2) + " あまり " + (7 % 2));
        System.out.println("7 ÷ 2 = " + (7.0 / 2));
    }
}
