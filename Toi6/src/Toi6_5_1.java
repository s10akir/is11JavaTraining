public class Toi6_5_1 {
    public static void main(String[] args) {
        int[] arraySeikai = new int[10];

        for (int i = 0; i < arraySeikai.length; i++) {
            arraySeikai[i] = 1;
        }

        for (int i = 0; i < arraySeikai.length; i++) {
            System.out.print(arraySeikai[i] + " ");
        }
        System.out.println();
    }
}
